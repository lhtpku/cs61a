def squares(s):
    """Returns a new list containing square roots of the elements of the
    original list that are perfect squares.

    >>> seq = [8, 49, 8, 9, 2, 1, 100, 102]
    >>> squares(seq)
    [7, 3, 1, 10]
    >>> seq = [500, 30]
    >>> squares(seq)
    []
    """
    "*** YOUR CODE HERE ***"
    ## math.sqrt is slower than **0.5, so this answer not very good.
    import math
    tmp = map(math.sqrt, s)
    return [int(k) for k in tmp if k==round(k)]

def key_of_min_value(d):
    """Returns the key in a dict d that corresponds to the minimum value of d.
    >>> letters = {'a': 6, 'b': 5, 'c': 4, 'd': 5}
    >>> min(letters)
    'a'
    >>> key_of_min_value(letters)
    'c'
    """
    "*** YOUR CODE HERE ***"
    # return min([x for x in d.items()], key=lambda x:x[1])[0]
    return min(d, key=lambda x:d[x])

def ten_pairs(n):
    """Return the number of ten-pairs within positive integer n.

    >>> ten_pairs(7823952)
    3
    >>> ten_pairs(55055)
    6
    >>> ten_pairs(9641469)
    6
    """
    "*** YOUR CODE HERE ***"
    if n<10:
        return 0
    ##
    tmp = {i:0 for i in range(10)}
    while n>0:
        tmp[n%10] += 1
        n //= 10
    res = 0
    for i in range(1,5):
        res += tmp[i]*tmp[10-i]
    return res if tmp[5]==0 else res+tmp[5]*(tmp[5]-1)//2
